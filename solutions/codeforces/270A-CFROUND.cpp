#include <bits/stdc++.h>

using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int n;
    cin >> n;
    if (n % 2 == 0)
        cout << 4 << " " << n - 4 << "\n";
    else
        cout << n - 9 << " " << 9 << "\n";
    return 0;
}
